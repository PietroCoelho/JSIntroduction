/**
 * Variaveis
 *
 * Nome simbolico para receber algum valor
 * Atalhos de codigo
 * Indentificadores
 *
 *  3 palavras reservadas para criar uma variavel
 *
 * var
 * let
 * const
 */

// var
var clima = "Quente"
clima = "frio"
console.log(clima)
// 
// let
let clima = "Quente"
clima = "frio"
console.log(clima)

// const -> Ela tem uma pequena diferença, o seu valor não pode ser alterado como as demais.
const clima = "Quente"
clima = "frio"
console.log(clima)

/**
 * O JS é uma linguagem fracamente tipada e dinamica
 *  -Variaveis não precisam ter um tipo previamente definido
 *  -Podemos mudar o conteudo da variavel
 */